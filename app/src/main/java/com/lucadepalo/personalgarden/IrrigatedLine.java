package com.lucadepalo.personalgarden;

public class IrrigatedLine {

    private int numLine, numPlace;

    public IrrigatedLine(int numLine, int numPlace) {
        this.numLine = numLine;
        this.numPlace = numPlace;
    }

    public int getNumLine() {
        return numLine;
    }

    public int getNumPLace() {
        return numPlace;
    }
}
